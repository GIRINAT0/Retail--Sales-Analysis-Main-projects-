-- Total Sales by Store
-- 4 Write a query to group sales (total_price) by each store_id.

 select store_id,sum(total_price) from orders
 inner join order_items
 group by store_id;
 
 --  5 Use ORDER BY and LIMIT to get the top 5 most sold products by quantity.

select product_name, sum(total_price) as sum_total_price  from order_items
group by product_name
order by sum_total_price desc
limit 5;

