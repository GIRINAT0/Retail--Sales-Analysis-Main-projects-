-- 9 Stock Alert Query
--  Write a query to list products where stock quantity < 10 in any store.

select p.product_name,s.store_id,s.product_id,s.quantity from products p
join stocks s
on p.product_id=s.product_id
where s.quantity <= 10
order by s.quantity desc;


